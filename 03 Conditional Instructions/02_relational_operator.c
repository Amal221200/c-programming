#include <stdio.h>
#include <stdbool.h>

int main() {
  // 01. Equal to
  bool cond1 = 6 == 6;
  
  // 02. Not Equal to
  bool cond2 = 6 != 6;
  
  // 03. Greater than
  bool cond3 = 6 > 6;
  
  // 04. Less than
  bool cond4 = 6 < 6;
  
  // 05. Greater than or equal to
  bool cond5 = 6 >= 6;
  
  // 06. Less than or equal to
  bool cond6 = 6 <= 6;
  
  return 0;
}
